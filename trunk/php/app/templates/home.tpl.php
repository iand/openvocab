<h1>Welcome to OpenVocab</h1>
<p class="summary">OpenVocab is a community maintained vocabulary 
intended for use on the Semantic Web</p>

<p>OpenVocab is ideal for properties and classes that don't warrant the 
effort of creating or maintaining a full schema. OpenVocab allows 
anyone to create and modify vocabulary terms using their web browser. 
Each term is described using appropriate elements of RDF, RDFS and OWL. 
OpenVocab allows you to create any properties and classes; assign 
labels, comments and descriptions; declare domains and ranges and much 
more.</p>

<p>Terms managed by OpenVocab conform to the following URI pattern:</p>
<p><code><? echo(VOCAB_NS); ?>{termname}</code></p>

<p>You can obtain different representations of a term's definition 
using URI's like these:</p>
<p><code><? echo(VOCAB_NS); ?>{termname}.html</code></p>
<p><code><? echo(VOCAB_NS); ?>{termname}.rdf</code></p>
<p><code><? echo(VOCAB_NS); ?>{termname}.turtle</code></p>
<p><code><? echo(VOCAB_NS); ?>{termname}.json</code></p>

<p>All data and content hosted by OpenVocab is gifted to the public 
domain and may be used for any purpose whatsoever. </p>

